def _build_toc(sections: list[dict], show_missing: bool) -> str:
    items = []
    for s in sections:
        sid = s["id"]
        title = html.escape(s["title"])
        status = s.get("status", "found")
        if status == "missing" and not show_missing:
            continue
        status_class = f"toc-status-{status}"
        icon = _status_icon(status)
        items.append(
            f'<li class="toc-item {status_class}">'
            f'<a href="#{sid}" class="toc-link" data-section="{sid}">'
            f'{icon}<span>{title}</span></a></li>'
        )
    return "\n".join(items)


def _status_icon(status: str) -> str:
    if status == "found":
        return '<span class="toc-icon toc-icon-found"><svg width="14" height="14" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2.5"><path d="M20 6L9 17l-5-5"/></svg></span>'
    if status == "partial":
        return '<span class="toc-icon toc-icon-partial"><svg width="14" height="14" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2.5"><circle cx="12" cy="12" r="10"/><line x1="12" y1="8" x2="12" y2="12"/><line x1="12" y1="16" x2="12.01" y2="16"/></svg></span>'
    return '<span class="toc-icon toc-icon-missing"><svg width="14" height="14" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2.5"><circle cx="12" cy="12" r="10"/><line x1="15" y1="9" x2="9" y2="15"/><line x1="9" y1="9" x2="15" y2="15"/></svg></span>'


def _render_section(
    section: dict,
    show_sources: bool,
    show_confidence: bool,
    show_missing: bool,
    accent_color: str,
) -> str:
    sid = section["id"]
    title = html.escape(section["title"])
    content = section.get("content", "")
    status = section.get("status", "found")
    source_urls = section.get("source_urls", [])
    confidence = section.get("confidence", 0)
    last_verified = section.get("last_verified")

    if status == "missing" and not show_missing:
        return ""

    status_badge = ""
    section_class = "section"
    if status == "missing":
        section_class += " section-missing"
        status_badge = (
            '<span class="status-badge status-missing">'
            '<svg width="14" height="14" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2">'
            '<path d="M10.29 3.86L1.82 18a2 2 0 001.71 3h16.94a2 2 0 001.71-3L13.71 3.86a2 2 0 00-3.42 0z"/>'
            '<line x1="12" y1="9" x2="12" y2="13"/><line x1="12" y1="17" x2="12.01" y2="17"/>'
            '</svg> Missing from website</span>'
        )
    elif status == "partial":
        section_class += " section-partial"
        status_badge = (
            '<span class="status-badge status-partial">'
            '<svg width="14" height="14" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2">'
            '<circle cx="12" cy="12" r="10"/><line x1="12" y1="8" x2="12" y2="12"/>'
            '<line x1="12" y1="16" x2="12.01" y2="16"/>'
            '</svg> Partial information</span>'
        )

    content_html = _markdown_to_html(content) if content else (
        '<p class="empty-msg">No information was found on the university website for this section.</p>'
    )

    sources_html = ""
    if show_sources and source_urls:
        chips = "".join(
            f'<a href="{html.escape(u)}" target="_blank" rel="noopener" class="source-chip">'
            f'<svg width="12" height="12" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2">'
            f'<path d="M18 13v6a2 2 0 01-2 2H5a2 2 0 01-2-2V8a2 2 0 012-2h6"/>'
            f'<polyline points="15 3 21 3 21 9"/><line x1="10" y1="14" x2="21" y2="3"/></svg>'
            f'{html.escape(_url_label(u))}</a>'
            for u in source_urls[:5]
        )
        sources_html = f'<div class="source-links"><span class="source-label">Sources:</span>{chips}</div>'

    confidence_html = ""
    if show_confidence:
        pct = int(confidence * 100)
        confidence_html = (
            f'<div class="confidence-bar-wrap">'
            f'<span class="confidence-label">Confidence: {pct}%</span>'
            f'<div class="confidence-track"><div class="confidence-fill" style="width:{pct}%"></div></div>'
            f'</div>'
        )

    verified_html = ""
    if last_verified:
        try:
            vd = datetime.fromisoformat(last_verified.replace("Z", "+00:00"))
            verified_html = f'<span class="verified-date">Last verified: {vd.strftime("%b %d, %Y")}</span>'
        except Exception:
            pass

    return f"""
    <section class="{section_class}" id="{sid}">
      <div class="section-header">
        <h2 class="section-title">{title}</h2>
        <div class="section-badges">{status_badge}{verified_html}</div>
      </div>
      <div class="section-body">{content_html}</div>
      {sources_html}
      {confidence_html}
    </section>"""


def _url_label(url: str) -> str:
    """Extract a short label from a URL."""
    url = url.rstrip("/")
    parts = url.split("/")
    if len(parts) > 3:
        return "/".join(parts[-2:])[:40]
    return url.split("//")[-1][:40]


def _markdown_to_html(text: str) -> str:
    """Simple markdown to HTML conversion for guide content."""
    if not text:
        return ""

    lines = text.split("\n")
    html_lines = []
    in_list = False
    in_table = False
    table_rows: list[str] = []

    for line in lines:
        stripped = line.strip()

        # Table row
        if stripped.startswith("|") and stripped.endswith("|"):
            if not in_table:
                in_table = True
                if in_list:
                    html_lines.append("</ul>")
                    in_list = False
                table_rows = []
            # Skip separator rows
            if re.match(r"^\|[\s\-:|]+\|$", stripped):
                continue
            cells = [c.strip() for c in stripped.strip("|").split("|")]
            table_rows.append(cells)
            continue
        elif in_table:
            # End table
            html_lines.append(_build_table(table_rows))
            in_table = False
            table_rows = []

        # Headings
        if stripped.startswith("### "):
            if in_list:
                html_lines.append("</ul>")
                in_list = False
            html_lines.append(f"<h4>{_inline_md(stripped[4:])}</h4>")
            continue
        if stripped.startswith("## "):
            if in_list:
                html_lines.append("</ul>")
                in_list = False
            html_lines.append(f"<h3>{_inline_md(stripped[3:])}</h3>")
            continue

        # Bullet list
        if re.match(r"^[-*]\s", stripped):
            if not in_list:
                in_list = True
                html_lines.append("<ul>")
            html_lines.append(f"<li>{_inline_md(stripped[2:])}</li>")
            continue

        # Numbered list
        if re.match(r"^\d+\.\s", stripped):
            content = re.sub(r"^\d+\.\s*", "", stripped)
            if not in_list:
                in_list = True
                html_lines.append("<ul class='numbered'>")
            html_lines.append(f"<li>{_inline_md(content)}</li>")
            continue

        # Close list if not a list item
        if in_list:
            html_lines.append("</ul>")
            in_list = False

        # Empty line -> paragraph break
        if not stripped:
            html_lines.append("")
            continue

        # Regular paragraph
        html_lines.append(f"<p>{_inline_md(stripped)}</p>")

    if in_list:
        html_lines.append("</ul>")
    if in_table and table_rows:
        html_lines.append(_build_table(table_rows))

    return "\n".join(html_lines)


def _inline_md(text: str) -> str:
    """Convert inline markdown (bold, italic, links, code)."""
    text = html.escape(text)
    # Bold
    text = re.sub(r"\*\*(.+?)\*\*", r"<strong>\1</strong>", text)
    # Italic
    text = re.sub(r"\*(.+?)\*", r"<em>\1</em>", text)
    # Code
    text = re.sub(r"`(.+?)`", r"<code>\1</code>", text)
    # Links - already escaped, so unescape the URL part
    text = re.sub(
        r"\[(.+?)\]\((.+?)\)",
        lambda m: f'<a href="{html.unescape(m.group(2))}" target="_blank" rel="noopener">{m.group(1)}</a>',
        text,
    )
    return text


def _build_table(rows: list[list[str]]) -> str:
    """Build an HTML table from parsed rows."""
    if not rows:
        return ""
    header = rows[0]
    body = rows[1:] if len(rows) > 1 else []
    thead = "<tr>" + "".join(f"<th>{_inline_md(c)}</th>" for c in header) + "</tr>"
    tbody_rows = []
    for row in body:
        cells = "".join(f"<td>{_inline_md(c)}</td>" for c in row)
        tbody_rows.append(f"<tr>{cells}</tr>")
    tbody = "\n".join(tbody_rows)
    return f'<div class="table-wrap"><table><thead>{thead}</thead><tbody>{tbody}</tbody></table></div>'


