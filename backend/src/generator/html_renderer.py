def _get_js() -> str:
    return """
(function() {
  // Scroll spy for TOC active state
  const sections = document.querySelectorAll('.section');
  const tocLinks = document.querySelectorAll('.toc-link');

  function updateActiveToc() {
    let current = '';
    sections.forEach(function(section) {
      const rect = section.getBoundingClientRect();
      if (rect.top <= 150) {
        current = section.id;
      }
    });
    tocLinks.forEach(function(link) {
      link.classList.toggle('active', link.getAttribute('data-section') === current);
    });
  }

  window.addEventListener('scroll', updateActiveToc, { passive: true });
  updateActiveToc();

  // Smooth scroll for TOC links
  tocLinks.forEach(function(link) {
    link.addEventListener('click', function(e) {
      e.preventDefault();
      var target = document.getElementById(this.getAttribute('data-section'));
      if (target) {
        target.scrollIntoView({ behavior: 'smooth', block: 'start' });
      }
      // Close mobile sidebar
      document.getElementById('guideSidebar').classList.remove('open');
    });
  });

  // Mobile TOC toggle
  var toggleBtn = document.getElementById('mobileTocToggle');
  var sidebar = document.getElementById('guideSidebar');
  if (toggleBtn) {
    toggleBtn.addEventListener('click', function() {
      sidebar.classList.toggle('open');
    });
  }

  // Close sidebar on outside click (mobile)
  document.addEventListener('click', function(e) {
    if (sidebar.classList.contains('open') &&
        !sidebar.contains(e.target) &&
        e.target !== toggleBtn) {
      sidebar.classList.remove('open');
    }
  });
})();
"""
