from datetime import datetime
from typing import Any


def render_guide_html(guide: dict[str, Any]) -> str:
    """Render a full admission guide as a self-contained HTML page."""
    university_name = guide.get("university_name", "University")
    generated_at = guide.get("generated_at", "")
    if isinstance(generated_at, datetime):
        generated_at = generated_at.strftime("%B %d, %Y")
    elif isinstance(generated_at, str) and generated_at:
        try:
            generated_at = datetime.fromisoformat(
                generated_at.replace("Z", "+00:00")
            ).strftime("%B %d, %Y")
        except Exception:
            pass

    sections = guide.get("sections", [])
    customization = guide.get("customization", {})
    primary_color = customization.get("primary_color", "#1e3a5f")
    accent_color = customization.get("accent_color", "#3b82f6")
    show_sources = customization.get("show_sources", True)
    show_confidence = customization.get("show_confidence", False)
    show_missing = customization.get("show_missing_sections", True)
